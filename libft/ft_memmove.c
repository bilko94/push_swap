#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char		*d;
	char		*s;
	size_t		i;

	i = -1;
	d = dst;
	s = (char*)src;
	if ((src == dst && len == 0) || (s == NULL && d == NULL))
		return (NULL);
	if (s < d)
		while ((int)(--len) >= 0)
			*(d + len) = *(s + len);
	else
		while (++i < len)
			*(d + i) = *(s + i);
	return (dst);
}
