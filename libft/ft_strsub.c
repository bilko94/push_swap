#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*ptr;

	if ((ptr = ft_strnew(len)) && s)
		return (ft_strncpy(ptr, &s[start], len));
	return (NULL);
}
